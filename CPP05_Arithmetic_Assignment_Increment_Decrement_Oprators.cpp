#include <iostream>

using namespace std;

int main()
{
    int a=10,b=5;

    cout << a + b << endl; //penjumlahan
    cout << a - b << endl; //pengurangan
    cout << a / b << endl; //pembagian
    cout << a * b << endl; //perkalian

    cout << a % b << endl; //modulo -> pengingat

    cout << ++a << endl; //Pre-Increment
    cout << --a << endl; //Pre-decrement
    cout << a++ << endl; //Post-Increment
    cout << a-- << endl; //Post-decrement
}
